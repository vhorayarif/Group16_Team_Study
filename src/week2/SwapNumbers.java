public class SwapNumbers {
    public static void main(String[] args) {

        int a = 4;
        int b = 10;

        a = a + b; // 14
        b = a - b; // 4
        a = a - b;

        System.out.println("a " + a);
        System.out.println("b " + b);

    }
}
